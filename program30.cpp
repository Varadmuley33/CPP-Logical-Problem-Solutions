#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints numbers from 1 to 5 using a while loop
//
/////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 1;                         // Initialization

    while(iCnt <= 5)                      // Loop condition
    {
        cout << iCnt << "\t";             // Print current number
        iCnt++;                           // Increment
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                            // Function call

    return 0;
}   // End of main

