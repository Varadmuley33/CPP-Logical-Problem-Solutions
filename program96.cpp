int main()
{
    int Arr[4];

    Arr[0] = 10;
    Arr[1] = 20;
    Arr[2] = 30;
    Arr[3] = 40;

    cout << Arr[0] << endl;
    cout << Arr[1] << endl;
    cout << Arr[2] << endl;
    cout << Arr[3] << endl;

    return 0;
}   // End of main
