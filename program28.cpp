#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints numbers 1 to 5 using tab spacing
//
/////////////////////////////////////////////////////////////////

void Display()
{
    cout << "1\t";
    cout << "2\t";
    cout << "3\t";
    cout << "4\t";
    cout << "5\t";

    cout << endl;                      // New line
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                         // Function call

    return 0;
}   // End of main

