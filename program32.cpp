#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints pattern: 1 * 2 * 3 * 4 * 5 *
//
/////////////////////////////////////////////////////////////////

void Display()
{
    cout << "1\t*\t";
    cout << "2\t*\t";
    cout << "3\t*\t";
    cout << "4\t*\t";
    cout << "5\t*\t";              // Pattern output
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                      // Function call

    return 0;
}   // End of main

