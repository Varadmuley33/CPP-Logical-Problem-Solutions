#include <iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int Arr[] = {10, 20, 30, 40};
    int i = 0;

    int iLength = sizeof(Arr) / sizeof(Arr[0]);

    cout << "Number of elements : " << iLength << endl;

    for(i = 0; i < iLength; i++)
    {
        cout << Arr[i] << endl;
    }

    return 0;
}   // End of main
