#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints numbers 5 4 3 2 1 using tab spacing
//
/////////////////////////////////////////////////////////////////

void Display()
{
    cout << "5\t";
    cout << "4\t";
    cout << "3\t";
    cout << "2\t";
    cout << "1\t";
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                             // Function call

    return 0;
}   // End of main

