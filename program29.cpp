#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints numbers 1 to 5 using a for loop
//
/////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;                           // Loop counter

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        cout << iCnt << "\t";               // Display current number
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                              // Function call

    return 0;
}   // End of main

