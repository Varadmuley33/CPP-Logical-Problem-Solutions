#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints '*' symbol five times using a loop
//
/////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;                          // Loop counter

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        cout << "*\t";                     // Print asterisk
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                             // Function call

    return 0;
}   // End of main

