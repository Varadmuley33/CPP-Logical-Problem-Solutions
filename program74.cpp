#include <iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iNo = 0;
    int i = 0;

    cout << "Enter number : " << endl;
    cin >> iNo;

    for(i = 1; i <= (iNo / 2); i++)
    {
        if((iNo % i) == 0)
        {
            cout << i << endl;
        }
    }

    return 0;
}   // End of main

