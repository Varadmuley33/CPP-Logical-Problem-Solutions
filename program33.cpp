#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Prints pattern: number followed by '*' using while loop
//
/////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 1;                          // Initialization

    while(iCnt <= 5)                       // Loop condition
    {
        cout << iCnt << "\t*\t";           // Print number and star
        iCnt++;                            // Increment
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Display();                             // Function call

    return 0;
}   // End of main

