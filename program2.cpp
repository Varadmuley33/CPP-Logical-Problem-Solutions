#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int    iValue = 10;         // Integer variable
    char   cValue = 'V';        // Character variable
    double dValue = 55.89;      // Double variable

    cout << "Integer Value : " << iValue << endl;
    cout << "Character Value : " << cValue << endl;
    cout << "Double Value : " << dValue << endl;

    return 0;
}   // End of main

