#include <iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iNo = 12;
    int i = 0;

    for(i = 1; i <= (iNo / 2); i++)
    {
        if((iNo % i) == 0)
        {
            cout << i << endl;
        }
    }

    return 0;
}   // End of main

