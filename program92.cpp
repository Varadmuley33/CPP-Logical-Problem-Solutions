#include <iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int Arr[] = {10, 20, 30, 40};

    int iLength = sizeof(Arr) / sizeof(Arr[0]);

    cout << "Number of elements : " << iLength << endl;
    cout << Arr[0] << endl;
    cout << Arr[1] << endl;
    cout << Arr[2] << endl;
    cout << Arr[3] << endl;

    return 0;
}   // End of main
